package domain

// ListResult struct for response
type ListResult struct {
	Title  string `json:"Title"`
	Year   string `json:"Year"`
	ImdbID string `json:"imdbID"`
	Type   string `json:"Type"`
	Poster string `json:"Poster"`
}

// DetailResult struct for response
type DetailResult struct {
	Title    string `json:"Title"`
	Year     string `json:"Year"`
	Rated    string `json:"Rated"`
	Released string `json:"Released"`
	Runtime  string `json:"Runtime"`
	Genre    string `json:"Genre"`
	Director string `json:"Director"`
	Writer   string `json:"Writer"`
	Actors   string `json:"Actors"`
	Plot     string `json:"Plot"`
	Language string `json:"Language"`
	Country  string `json:"Country"`
	Awards   string `json:"Awards"`
	Poster   string `json:"Poster"`
	Ratings  []struct {
		Source string `json:"Source"`
		Value  string `json:"Value"`
	} `json:"Ratings"`
	Metascore  string `json:"Metascore"`
	ImdbRating string `json:"imdbRating"`
	ImdbVotes  string `json:"imdbVotes"`
	ImdbID     string `json:"imdbID"`
	Type       string `json:"Type"`
	DVD        string `json:"DVD"`
	BoxOffice  string `json:"BoxOffice"`
	Production string `json:"Production"`
	Website    string `json:"Website"`
	Response   string `json:"Response"`
}

// SearchRequest struct for request
type SearchRequest struct {
	Search string
	Page   int
}

// SearchResponse struct for the Omdb API
type SearchResponse struct {
	Search       []ListResult `json:"Search"`
	TotalResults string       `json:"totalResults"`
	Response     string       `json:"Response"`
}

// MovieRepository interface for repository
type MovieRepository interface {
	Fetch(query *SearchRequest) (res SearchResponse, err error)
	GetByID(id string) (res DetailResult, err error)
}
